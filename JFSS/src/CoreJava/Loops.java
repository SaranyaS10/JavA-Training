package CoreJava;

public class Loops {
	public static void main(String args[]){ 
		  for(int i=1;i<=1;i++){  
		        System.out.println(i*i);  
		    } 
		int j=1;
		while(j<=10){
		System.out.println(j+"*3="+j*3); 
		j++;
		}
		int K=1;
		do
		{
		System.out.println(K+"*4="+K*4); 
		K++;
		}while(K<=10);
		} 
}
