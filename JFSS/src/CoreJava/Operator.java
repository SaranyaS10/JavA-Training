package CoreJava;

public class Operator {
	public static void main(String args[])
	{
	int a,b,c;
	a=6;
	b=800;
	c=8;
	System.out.println(a+b/c);
	System.out.println(a+b-c);
	System.out.println(a*b);
	System.out.println(b/c);
	System.out.println(b*c/100);
	System.out.println(b==c);
	System.out.println(b!=c);
	}
}