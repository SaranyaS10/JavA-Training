package CoreJava;

public class WrapperDemo {
	  public static void main(String args[]){
		  int a=100;
		  Integer b=a;
		  System.out.println("Int a ="+a+"\nInteger b ="+b);
		  
		  Character c='A';
		  char d=c;
		  System.out.println("Character c ="+c+"\nChar d ="+d);
		  
	      Double f=10.230;
		  double e=f;
		  System.out.println("Double f ="+f+"\ndouble e ="+e);

          boolean B=true;
          Boolean g=B;
          System.out.println(" boolean B ="+B+"\nBoolean g ="+g );
          
		  
	  }
}
